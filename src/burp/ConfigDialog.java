    private void jButtonImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImportActionPerformed
        try
        {
            new JSONObject(new JSONTokener(jTextArea1.getText()));
        }
        catch (JSONException e)
        {
            JOptionPane.showMessageDialog(this, "The configuration must be valid JSON", BurpExtender.name, JOptionPane.WARNING_MESSAGE);
            return;
        }
        JDialog dialog = new ConfigImport(this);
        dialog.setLocationRelativeTo(replicatorPanel);
        dialog.setVisible(true);
    }//GEN-LAST:event_jButtonImportActionPerformed

