    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        try
        {
            new JSONObject(new JSONTokener(jTextArea1.getText()));
            replicatorPanel.config = jTextArea1.getText();
            dispose();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "The configuration must be valid JSON", BurpExtender.name, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

